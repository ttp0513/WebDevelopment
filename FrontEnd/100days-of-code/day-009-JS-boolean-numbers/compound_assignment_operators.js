// Level 1: Start with basic stats
let score = 10;
console.log("Starting score:", score);

// Addition assignment: Add 5 points
score += 5; // score = score + 5
console.log("After += 5 →", score); // 15

// Subtraction assignment: Lose 3 points
score -= 3; // score = score - 3
console.log("After -= 3 →", score); // 12

// Multiplication assignment: Multiply by 2
score *= 2; // score = score * 2
console.log("After *= 2 →", score); // 24

// Division assignment: Divide by 4
score /= 4; // score = score / 4
console.log("After /= 4 →", score); // 6

// Modulus assignment: Get remainder after dividing by 4
score %= 4; // score = score % 4
console.log("After %= 4 →", score); // 2

// Exponentiation assignment: Raise to the power of 3
score **= 3; // score = score ** 3
console.log("After **= 3 →", score); // 8


